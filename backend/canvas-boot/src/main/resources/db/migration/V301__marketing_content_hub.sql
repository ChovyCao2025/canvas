CREATE TABLE IF NOT EXISTS marketing_content_entry (
  id BIGINT AUTO_INCREMENT PRIMARY KEY,
  tenant_id BIGINT NOT NULL,
  entry_key VARCHAR(128) NOT NULL,
  content_type VARCHAR(64) NOT NULL,
  title VARCHAR(200) NOT NULL,
  slug VARCHAR(200) NULL,
  locale VARCHAR(32) NULL,
  summary VARCHAR(1000) NULL,
  body_json JSON NOT NULL,
  seo_json JSON NOT NULL,
  asset_refs_json JSON NOT NULL,
  status VARCHAR(32) NOT NULL DEFAULT 'DRAFT',
  published_at DATETIME NULL,
  created_by VARCHAR(128) NOT NULL,
  updated_by VARCHAR(128) NOT NULL,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  UNIQUE KEY uk_marketing_content_entry_key (tenant_id, entry_key),
  UNIQUE KEY uk_marketing_content_entry_slug (tenant_id, content_type, locale, slug),
  INDEX idx_marketing_entry_search (tenant_id, content_type, status, updated_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='Marketing CMS-style content entries';

