  KEY `idx_programmatic_dsp_supply_path_line_item` (`tenant_id`, `line_item_id`, `exchange_key`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='Programmatic DSP supply path transparency and deal governance';

CREATE TABLE IF NOT EXISTS `programmatic_dsp_performance_snapshot` (
  `id` BIGINT AUTO_INCREMENT PRIMARY KEY,
  `tenant_id` BIGINT NOT NULL,
  `seat_id` BIGINT NOT NULL,
  `campaign_id` BIGINT NOT NULL,
  `line_item_id` BIGINT NOT NULL,
  `snapshot_date` DATE NOT NULL,
  `bid_count` BIGINT NOT NULL DEFAULT 0,
  `win_count` BIGINT NOT NULL DEFAULT 0,
  `impression_count` BIGINT NOT NULL DEFAULT 0,
  `click_count` BIGINT NOT NULL DEFAULT 0,
  `conversion_count` BIGINT NOT NULL DEFAULT 0,
  `viewable_impression_count` BIGINT NOT NULL DEFAULT 0,
  `spend_amount` DECIMAL(18,4) NOT NULL DEFAULT 0.0000,
  `revenue_amount` DECIMAL(18,4) NOT NULL DEFAULT 0.0000,
  `metadata_json` JSON NULL,
  `created_by` VARCHAR(128) NULL,
  `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  UNIQUE KEY `uk_programmatic_dsp_snapshot_daily` (`tenant_id`, `seat_id`, `campaign_id`, `line_item_id`, `snapshot_date`),
  KEY `idx_programmatic_dsp_snapshot_scope` (`tenant_id`, `snapshot_date`, `seat_id`, `campaign_id`),
  KEY `idx_programmatic_dsp_snapshot_line_item` (`tenant_id`, `line_item_id`, `snapshot_date`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='Programmatic DSP daily delivery and performance snapshots';
