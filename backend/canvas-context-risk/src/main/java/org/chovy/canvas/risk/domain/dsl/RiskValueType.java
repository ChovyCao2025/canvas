package org.chovy.canvas.risk.domain.dsl;

/**
 * 风控规则值类型。
 */
public enum RiskValueType {
    /** 字符串。 */
    /**
     * 表示 成员 枚举值。
     */
    STRING,
    /** 整数。 */
    /**
     * 表示 成员 枚举值。
     */
    INTEGER,
    /** 小数。 */
    /**
     * 表示 成员 枚举值。
     */
    DECIMAL,
    /** 布尔值。 */
    /**
     * 表示 成员 枚举值。
     */
    BOOLEAN,
    /** 日期时间。 */
    /**
     * 表示 成员 枚举值。
     */
    DATETIME,
    /** 字符串集合。 */
    /**
     * 表示 成员 枚举值。
     */
    STRING_SET,
    /** 数值集合。 */
    NUMBER_SET
}
