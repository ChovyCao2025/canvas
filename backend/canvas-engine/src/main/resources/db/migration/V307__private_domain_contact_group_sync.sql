CREATE TABLE IF NOT EXISTS conversation_private_group (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    tenant_id BIGINT NOT NULL DEFAULT 0 COMMENT 'Tenant id',
    provider VARCHAR(64) NOT NULL COMMENT 'Private-domain provider such as WECOM',
    external_group_id VARCHAR(128) NOT NULL COMMENT 'Provider customer group id',
    name VARCHAR(255) NOT NULL COMMENT 'Customer group name',
    owner_user_id VARCHAR(128) NULL COMMENT 'Group owner user id',
    status VARCHAR(32) NOT NULL DEFAULT 'ACTIVE' COMMENT 'ACTIVE/DISMISSED/UNKNOWN',
    member_count INT NOT NULL DEFAULT 0 COMMENT 'Provider reported member count',
    created_at_remote DATETIME NULL COMMENT 'Provider group creation time',
    raw_payload_json JSON NULL COMMENT 'Provider raw group payload',
    synced_at DATETIME NOT NULL COMMENT 'Latest sync time',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    UNIQUE KEY uk_conversation_private_group (tenant_id, provider, external_group_id),
    INDEX idx_conversation_private_group_owner (tenant_id, provider, owner_user_id),
    INDEX idx_conversation_private_group_status (tenant_id, provider, status, synced_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='SCRM private-domain customer groups';

