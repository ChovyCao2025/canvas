CREATE TABLE IF NOT EXISTS conversation_private_contact (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    tenant_id BIGINT NOT NULL DEFAULT 0 COMMENT 'Tenant id',
    provider VARCHAR(64) NOT NULL COMMENT 'Private-domain provider such as WECOM',
    external_contact_id VARCHAR(128) NOT NULL COMMENT 'Provider external contact id',
    user_id VARCHAR(192) NOT NULL COMMENT 'Stable conversation user id, provider:external_contact_id',
    display_name VARCHAR(128) NULL COMMENT 'Contact display name',
    avatar_url VARCHAR(512) NULL COMMENT 'Contact avatar URL',
    corp_name VARCHAR(255) NULL COMMENT 'External company name',
    gender VARCHAR(32) NULL COMMENT 'Provider-normalized gender',
    union_id_hash VARCHAR(128) NULL COMMENT 'Hashed union id or cross-channel id',
    tags_json JSON NULL COMMENT 'Contact tags',
    attributes_json JSON NULL COMMENT 'Normalized contact attributes',
    raw_payload_json JSON NULL COMMENT 'Provider raw payload for audit',
    synced_at DATETIME NOT NULL COMMENT 'Latest sync time',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    UNIQUE KEY uk_conversation_private_contact (tenant_id, provider, external_contact_id),
    INDEX idx_conversation_private_contact_user (tenant_id, user_id),
    INDEX idx_conversation_private_contact_name (tenant_id, provider, display_name),
    INDEX idx_conversation_private_contact_synced (tenant_id, provider, synced_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='SCRM private-domain contacts';

