CREATE TABLE IF NOT EXISTS conversation_private_sync_run (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    tenant_id BIGINT NOT NULL DEFAULT 0 COMMENT 'Tenant id',
    provider VARCHAR(64) NOT NULL COMMENT 'Private-domain provider such as WECOM',
    sync_type VARCHAR(32) NOT NULL COMMENT 'CONTACTS/GROUPS/FULL',
    status VARCHAR(32) NOT NULL DEFAULT 'RUNNING' COMMENT 'RUNNING/SUCCESS/FAILED',
    requested_by VARCHAR(128) NOT NULL DEFAULT 'system' COMMENT 'Operator or sync worker',
    source_cursor VARCHAR(512) NULL COMMENT 'Provider source cursor',
    next_cursor VARCHAR(512) NULL COMMENT 'Provider next cursor',
    contact_count INT NOT NULL DEFAULT 0 COMMENT 'Contacts received',
    contact_upserted INT NOT NULL DEFAULT 0 COMMENT 'Contacts upserted',
    group_count INT NOT NULL DEFAULT 0 COMMENT 'Groups received',
    group_upserted INT NOT NULL DEFAULT 0 COMMENT 'Groups upserted',
    member_count INT NOT NULL DEFAULT 0 COMMENT 'Members received',
    member_upserted INT NOT NULL DEFAULT 0 COMMENT 'Members upserted',
    failed_count INT NOT NULL DEFAULT 0 COMMENT 'Failed records',
    error_message VARCHAR(1024) NULL COMMENT 'Failure message',
    metadata_json JSON NULL COMMENT 'Sync metadata',
    started_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    completed_at DATETIME NULL,
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_conversation_private_sync_run_provider (tenant_id, provider, sync_type, created_at),
    INDEX idx_conversation_private_sync_run_status (tenant_id, status, created_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='SCRM private-domain sync runs';
